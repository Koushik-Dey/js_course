const month = "april";

switch (month) {
  case "jan":
    console.log("january");
    break;
  case "feb":
    console.log("February");
    break;
  case "mar":
    console.log("March");
    break;
  case "april":
    console.log("April");
    break;

  default:
    console.log("default case match");
    break;
}
